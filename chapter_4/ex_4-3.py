# Exercise 4.3: Counting to Twenty:  
#               Use a for loop to print the numbers from 1 to 20, inclusive.


def main():

    # For loop to print numbers from 1 to 20
    print("\nPrinting numbers from 1 to 20:")
    for i in range(1,21):
        print(i)

if __name__ == '__main__':
    main()

